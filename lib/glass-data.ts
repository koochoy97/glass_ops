// Definición de tipos para los datos de vidrio
export interface GlassType {
  name: string
  code: string
  width: number
  height: number
  price: number
  thickness?: number
  description?: string
}

// Función para calcular el precio ajustado según la fórmula:
// Restar 3.5%, luego aumentar 10.5%, y finalmente aumentar 30%
function adjustPrice(originalPrice: number): number {
  // Paso 1: Restar 3.5%
  const afterDiscount = originalPrice * (1 - 0.035)

  // Paso 2: Aumentar 10.5%
  const afterFirstIncrease = afterDiscount * (1 + 0.105)

  // Paso 3: Aumentar 30%
  const finalPrice = afterFirstIncrease * 1.3

  // Redondear a 2 decimales
  return Math.round(finalPrice * 100) / 100
}

